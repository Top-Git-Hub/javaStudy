public class Main {
    public static void main(String[] args) {
        int a=10;
        a++;
        //自增运算符
        System.out.println(a);
        int b=10;
        ++b;
        System.out.println(b);
        b--;
        System.out.println(b);
        //自减运算符
        //"++"与"--"无论是放在变量前还是后面，单独写一行所得到的值都一样的

    }
}