public class Main {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a==b);
        //==左右两边的值是否相等，成立为true，否则为false
        System.out.println(a!=b);
        //!=:判断左右两边的值是否不相等，成立为true，否则为false
        System.out.println(a>b);
        //>:判断左边是否大于右边，成立为true，否则为false
        System.out.println(a>=b);
        //>=:判断左边是否大于等于右边，成立为true，否则为false
        System.out.println(a<b);
        //<:判断左边是否小于右边，成立为true，否则为false
        System.out.println(a<=b);
        //<=:判断左边是否小于等于右边，成立为true，否则为false
        //关系运算符都是boolen类型

    }
}
