public class StringDemo8 {
    private int  year;
    private int month;
    private int day;
    private int  gender;

    public StringDemo8() {
    }

    public StringDemo8(int year, int month, int day, int gender) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.gender = gender;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }
}
