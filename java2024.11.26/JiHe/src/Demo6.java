public class Demo6 {
    private String bound;
    private int price;

    public Demo6() {
    }

    public Demo6(String bound, int price) {
        this.bound = bound;
        this.price = price;
    }

    public String getBound() {
        return bound;
    }

    public void setBound(String bound) {
        this.bound = bound;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
